// Fonction pour obtenir les coordonnées d'un code postal
export function getPostcodeCoords(cp) {
  // Nettoyer le CP (enlever les espaces, convertir en string)
  const cleanCP = String(cp).trim().replace(/\s/g, '');
  
  // Chercher dans la map
  if (postcodeCoordsMap[cleanCP]) {
    return postcodeCoordsMap[cleanCP];
  }
  
  // Si pas trouvé, retourner Paris par défaut (centre de France)
  return [48.8566, 2.3522];
}

// Fonction pour vérifier si un CP existe
export function postCodeExists(cp) {
  const cleanCP = String(cp).trim().replace(/\s/g, '');
  return postcodeCoordsMap.hasOwnProperty(cleanCP);
}

